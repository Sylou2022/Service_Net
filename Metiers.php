  <h2 style="text-align:center;color: black;text-align:center; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif;" > LE METIER DU MACON : </h2>
  <br>
  <p style="text-align:justify;color:black; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif; width: 900px;margin-left:150px;">Dans la construction traditionnelle, le maçon prépare les fondations, monte les murs, les cloisons, pose les dalles. Il assemble briques, parpaings ou pierres avec des produits liants comme le ciment ou le mortier. Il doit respecter les plans qui lui sont fournis, les niveaux, l’aplomb.
 Dans la construction industrielle, il utilise des armatures pour le ferraillage, installe et parfois effectue des coffrages et des moules dans lesquels il coule du béton. 
 Le maçon travaille seul ou en équipe sur les chantiers en fonction de leur importance. Il est généralement debout sur des échafaudages, dans des excavations ou des tranchées creusées dans le sol. Il porte des charges et doit gravir des échelles. 
 <br>
 Cependant, les conditions d’exercice du métier se sont nettement améliorées avec l’évolution des techniques et de la réglementation. Intervenant au niveau du gros œuvre le maçon travaille la plupart du temps en plein air et par (presque) tous les temps.
Le maçon travaille le plus souvent dans une entreprise, généralement sous la direction d’un chef de chantier. Des déplacements parfois importants sont à prévoir lorsqu’il est employé sur un grand chantier de travaux publics.</p>
