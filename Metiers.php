<h2 style="text-align:center;color: black;text-align:center; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif;"> Covoiturage : </h2>
<br>
<p style="text-align:justify;color:black; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif; width: 900px;margin-left:150px;"> Le covoiturage consiste en l’utilisation d’un véhicule de transport terrestre (le plus souvent une voiture) partagé par le conducteur avec un ou plusieurs passagers, dans le cadre d’un trajet effectué par le conducteur à son propre compte et qui ne donne lieu à aucune rétribution autre que celle du partage des frais.
<br>
Il procure des avantages individuels (partager les dépenses de carburant et de maintenance, agrémenter les voyages, développer le lien social) et collectifs (augmenter le taux de remplissage des véhicules, diminuer les embouteillages et la pollution).
<br> </p>
