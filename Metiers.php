<h2 style="text-align:center;color: black;text-align:center; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif;"> Tapissier : </h2>
<br>
<p style="text-align:justify;color:black; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif; width: 900px;margin-left:150px;"> Le tapissier garnisseur est l'artisan qui met en place la tapisserie d'ameublement, réalise des garnitures et des couvertures de sièges ou tout autre meuble recouvert de tissu ou de cuir.
<br>
Le tapissier / la tapissière d’ameublement habille et tapisse toutes sortes de meubles, notamment des sièges, de style ou contemporains. Il/elle collabore aussi à des travaux de décoration, en posant du tissu aux murs ou en garnissant les fenêtres de rideaux ou de voilages. Tout connaître sur les missions du tapissier / de la tapissière, sa formation, son salaire et les qualités nécessaires pour exercer ce métier créatif.</p>
