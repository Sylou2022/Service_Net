<h2 style="text-align:center;color: black;text-align:center; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif;"> LE PEINTRE : </h2>
<br>
<p style="text-align:justify;color:black; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif; width: 900px;margin-left:150px;">Un peintre en bâtiment est la personne responsable de l'application des peintures sur un bâtiment. Il habille les murs et les plafonds, maîtrise l’art de la finition et assure l'entretien des habitations.

  Le peintre en bâtiment doit reconnaître et savoir préparer tout type de support tel que le béton, le plâtre, le bois, la brique, le métal afin d'obtenir la finition demandée. Les diverses préparations permettront de rendre le support propre, sain, lisse avec une surface plane, et apte à recevoir la peinture. <br> Le peintre en bâtiment est le plus souvent le dernier ouvrier à intervenir sur un chantier, seul ou en équipe selon la taille des travaux à effectuer.
   Il peut travailler autant sur des chantiers de rénovation que sur des chantiers de construction neuve.</p>
