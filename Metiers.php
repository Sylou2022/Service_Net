<h2 class="texte" style="text-align:center;color: black;text-align:center; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif;"> LE CARRELAGE : </h2>
<br>
<p class="text-center" style="text-align:justify;color:black; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif; width: 900px;margin-left:150px;"> Le carreleur apporte les finitions à un mur, un sol en y aposant des carreaux décoratifs. Jonglant avec les couleurs et les matières, il apporte la touche finale à une pièce, lui offre une identité. Il est un travailleur manuel doté d'un fort sens de l'esthétisme. 
  Aujourd'hui, la demande est telle que le carreleur n'a pas à redouter des périodes d'inactivité. Avis aux candidats désireux de mêler tradition et créativité.
<br>
  Quelles sont les missions du carreleur ?
  Le carreleur est en charge de la pose des carrelages et mosaïques, que ce soit sur un chantier de construction (finitions) ou dans le cadre d'une redécoration d'intérieur. C'est à lui que revient donc de : 
  <br>
   Préparer les surfaces à couvrir, en retirant éventuellement les carreaux déjà présents
  définir un plan de pose, en liaison avec d'autres interlocuteurs (chauffagiste, plombier, électricien, peintre, etc.)
  Couper les tablettes en fonction des caractéristiques (forme, surface) des plans à couvrir </p>

