<h2 style="text-align:center;color: black;text-align:center; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif;"> LE SERVICE TRAITEUR : </h2>
<br>

<p style="text-align:justify;color:black; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif; width: 900px;margin-left:150px;">Le catering est un terme professionnel qui désigne l'approvisionnement en nourriture et boissons d'un groupe de personnes dans n'importe quel lieu. 
  Le service traiteur en fait partie, mais dans ce cas, la consommation de nourriture joue un rôle secondaire. 
  En effet, il s'agit principalement de l'effet crée auprès des invités. Nous entendons par là la prise en charge culinaire et organisationnelle d'un événement festif.
   Nos services soutiennent et concrétisent les objectifs fixés par le client et visent à créer des souvenirs chaleureux que les invités conserveront encore longtemps après l'événement en question.
   <br>
   Un événement se passe à un moment précisément défini et en un lieu donné. Grâce à une planification méticuleuse, le service traiteur de votre choix veille au déroulement efficace, exact et fiable de la manifestation.</p>
 

