<h2 style="text-align:center;color: black;text-align:center; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif;"> Electricien Batiment : </h2>
<br>
<p style="text-align:justify;color:black; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif; width: 900px;margin-left:150px;">
  L’électricien est un ouvrier du bâtiment chargé de la conception, de l’installation, de l’entretien et du dépannage des réseaux et équipements électriques. Outre les travaux d’installation électrique dans les logements, les bureaux ou les bâtiments industriels, il procède également à la distribution et aux raccordements aux appareils électriques.
 
 <br> En neuf, l’électricien se base sur des plans et des schémas de montage pour déterminer l’emplacement des différents éléments de l’installation électrique. Il va ensuite installer les canalisations et les supports, poser les câbles, mettre en place les différents appareillages tels que les tableaux, les armoires, les prises ou les appareils de chauffage et en effectuer les raccordements.
  Puis il procède à la mise en service. C’est également lui qui va procéder à la mise aux normes de ces installations via une série de tests. Son rôle en termes de sécurité est donc 
  
En rénovation, il est chargé de remplacer les anciennes installations électriques pour en assurer la conformité aux normes.</p>
