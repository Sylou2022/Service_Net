<h2 style="text-align:center;color: black;text-align:center; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif;"> LA PLOMBERIE : </h2>
<br>
<p style="text-align:justify;color:black; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif; width: 900px;margin-left:150px;">Un plombier possède un large éventail d’activités. En effet, il peut intervenir aussi bien en plomberie sanitaire qu’en chauffage, en climatisation ou encore sur des VMC (Ventilation Mécanique Contrôlée). Son travail peut aussi bien s’effectuer sur des appareils électriques ; au gaz, au fioul ou encore au bois. 
  Il existe la plomberie pour l’eau froide sanitaire. L’eau qui arrive aux habitations est froide, elle est ensuite redistribuée à tous les appareils de la maison nécessitant de l’eau. La nourrice est un objet permettant de raccorder cette arrivée d’eau à plusieurs départs pour alimenter les différents appareils sanitaires. 
 <br> Cette nourrice sert souvent à alimenter des dispositifs d’eau chaude sanitaire. Le plombier est aussi en capacité d’installer des réseaux d’évacuation des eaux usées reliés à une fosse toutes eaux ou encore au tout à l’égout. Enfin, il y a la plomberie pour le chauffage.</p>
