<h2 style="text-align:center;color: black;text-align:center; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif;">INFORMATIQUE : </h2>
<br>
<p style="text-align:justify;color:black; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif; width: 900px;margin-left:150px;">L'informatique s’intéresse à la mise en œuvre de méthodes scientifiques pour traiter l'information au moyen d'ordinateurs. Elle enrichit notamment les domaines suivants :
<br>
  Gestion des entreprises et les échanges financiers
  Communications de toutes sortes
  Contrôle des robots dans les usines et la conception de machines </p>
  
  <p style="text-align:justify;color:black; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif; width: 900px;margin-left:150px;"> L'intelligence artificielle: la compréhension des images, de la vidéo, du language, de la robotique
  Synthèse d'images pour le cinéma ou la télévision
  Reconnaissance de formes sur des images satellites ou médicales
  <br>
  Traitement du langage naturel et la déduction automatique
  Calculs scientifiques ou les techniques complexes
  Outre ses aspects appliqués, l'informatique bénéficie d'un support théorique important qui ne cesse de se consolider. Les théoriciens essaient, par exemple, de découvrir des limites intrinsèques à ce qu'il est possible de faire efficacement par ordinateur, et de résumer les phénomènes de l'informatique sous forme de lois et de théorèmes.</p>
