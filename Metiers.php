<h2 style="text-align:center;color: black;text-align:center; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif;"> Mécanicien : </h2>
<br>
<p style="text-align:justify;color:black; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif; width: 900px;margin-left:150px;"> Le mécanicien automobile est un technicien d’atelier qui assure l’entretien, la maintenance (prévention des pannes), la réparation et le réglage des véhicules automobiles. C’est le responsable des organes mécaniques constituant l’essentiel d’un véhicule : moteur, boîte de vitesses, embrayage, suspension, direction.
<br>
Il possède des notions pointues dans les domaines électrique, hydraulique, pneumatique ou encore électronique. Il peut aussi être amené à intervenir sur des activités de vente de voitures neuves ou d’occasion.</p>
  
