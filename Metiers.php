<h2 style="text-align:center;color: black;text-align:center; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif;"> La MENUISERIE : </h2>
<br>
<p style="text-align:justify;color:black; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif; width: 900px;margin-left:150px;">La menuiserie industrielle est une branche spécialisée dans la fabrication d’éléments en plexiglas, en PVC, en plastique, en aluminium, en acier ou encore en matières composites.
   Les éléments fabriqués par les entreprises de menuiserie industrielle sont essentiellement destinés au secteur du bâtiment. Il s’agit de portes et huisseries, fenêtres et encadrements, revêtements extérieurs et intérieurs, portails, serrurerie, charpentes, escaliers...</p>
