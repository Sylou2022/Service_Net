<h2 style="text-align:center;color: black;text-align:center; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif;"> LA COUTURE : </h2>
<br>
<p style="text-align:justify;color:black; font-family:'Lucida Sans', 'Lucida Sans Regular', 'Lucida Grande', 'Lucida Sans Unicode', Geneva, Verdana, sans-serif; width: 900px;margin-left:150px;">La couture est un métier. Ses principales tâches sont la confection, la retouche ou l'ajustement de vêtements. Généralement, le couturier est appelé à exercer son métier dans un établissement spécialisé en couture, chez un teinturier, dans une manufacture de vêtements, ou encore dans une boutique de vêtements habillés, lesquelles emploient habituellement leurs propres couturiers.</p>

